package com.jwt.jsonwebtokenproject.Response;



public class Token {
    private String jwtToken;

    public Token(String jwtToken) {
        this.jwtToken = jwtToken;
    }

    public String getJwtToken() {
        return jwtToken;
    }

    public void setJwtToken(String jwtToken) {
        this.jwtToken = jwtToken;
    }
}
